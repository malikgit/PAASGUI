package com.getusroi.paas.vpc.jaxb;

public class UnableToUnmarshalVPC extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UnableToUnmarshalVPC() {
		super();
	}

	public UnableToUnmarshalVPC(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnableToUnmarshalVPC(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToUnmarshalVPC(String message) {
		super(message);
	}

	public UnableToUnmarshalVPC(Throwable cause) {
		super(cause);
	}
	
}
